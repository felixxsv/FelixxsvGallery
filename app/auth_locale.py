def normalize_auth_language(value: str | None) -> str | None:
    raw = str(value or "").strip().lower().replace("_", "-")
    if raw == "":
        return None
    normalized = _ALIASES.get(raw)
    if normalized is None and "-" in raw:
        normalized = _ALIASES.get(raw.split("-", 1)[0])
    if normalized not in SUPPORTED_AUTH_LOCALES:
        return None
    return normalized


