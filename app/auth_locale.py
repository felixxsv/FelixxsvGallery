def localize_auth_message(message: str | None, language: str | None) -> str:
    text = str(message or "").strip()
    if text == "":
        return ""
    if normalize_auth_language(language) != "ko":
        return text
    localized = _KO_EXACT.get(text)
    if localized:
        return localized
    for pattern, formatter in _KO_REGEX:
        matched = pattern.match(text)
        if matched:
            return formatter(matched)
    return text


def localize_field_errors(field_errors: list[dict] | None, language: str | None) -> list[dict]:
    localized = []
    for item in field_errors or []:
        if not isinstance(item, dict):
            localized.append(item)
            continue
        copied = dict(item)
        copied["message"] = localize_auth_message(copied.get("message", ""), language)
        localized.append(copied)
    return localized


def localize_service_result(result: dict, language: str | None) -> dict:
    if not isinstance(result, dict):
        return result
    copied = dict(result)
    copied["message"] = localize_auth_message(copied.get("message", ""), language)
    copied["field_errors"] = localize_field_errors(copied.get("field_errors"), language)
    return copied
